#!/bin/bash
# Walks assets/images/photography/ and emits _data/photo_exif.yml
# keyed by filename. Run whenever you add or remove photos.
# Skipped silently if a file has no embedded camera EXIF.

set -e
ROOT="$(cd "$(dirname "$0")/.." && pwd)"
DIR="$ROOT/assets/images/photography"
OUT="$ROOT/_data/photo_exif.yml"

format_exif() {
  local path="$1"
  local meta
  meta=$(mdls "$path" 2>/dev/null)

  local model lens fnum exp iso
  model=$(echo "$meta" | awk -F'"' '/kMDItemAcquisitionModel/{print $2}')
  lens=$(echo  "$meta" | awk -F'"' '/kMDItemLensModel/{print $2}')
  fnum=$(echo  "$meta" | awk '/kMDItemFNumber/{print $3}')
  exp=$(echo   "$meta" | awk '/kMDItemExposureTimeSeconds/{print $3}')
  iso=$(echo   "$meta" | awk '/kMDItemISOSpeed/{print $3}')

  [ -z "$model" ] && { echo ""; return; }
  model=$(echo "$model" | sed 's/LEICA/Leica/')

  if [ -n "$lens" ] && [[ "$lens" =~ /([0-9]+)$ ]]; then
    focal="${BASH_REMATCH[1]}"
    base=$(echo "$lens" | sed -E 's/ ?1:[0-9.]+\/[0-9]+$//')
    lens="${base} ${focal}mm"
  fi

  local parts=()
  [ -n "$model" ] && parts+=("$model")
  [ -n "$lens" ]  && parts+=("$lens")
  [ -n "$fnum" ] && [ "$fnum" != "(null)" ] && parts+=("f/$fnum")
  if [ -n "$exp" ] && [ "$exp" != "(null)" ]; then
    shutter=$(awk -v e="$exp" 'BEGIN{
      if (e+0 == 0) {print ""; exit}
      if (e >= 1) {printf "%ss", e}
      else        {printf "1/%ds", int(1/e + 0.5)}
    }')
    [ -n "$shutter" ] && parts+=("$shutter")
  fi
  [ -n "$iso" ] && [ "$iso" != "(null)" ] && parts+=("ISO ${iso%.*}")

  local out=""
  local first=1
  for p in "${parts[@]}"; do
    if [ $first -eq 1 ]; then out="$p"; first=0
    else out="$out · $p"; fi
  done
  echo "$out"
}

echo "# auto-generated by bin/generate-photo-exif.sh — do not edit by hand" > "$OUT"
echo "# keyed by filename in assets/images/photography/" >> "$OUT"
echo ""  >> "$OUT"

count=0
for f in "$DIR"/*.jpg "$DIR"/*.jpeg "$DIR"/*.JPG; do
  [ -f "$f" ] || continue
  fn=$(basename "$f")
  cap=$(format_exif "$f")
  if [ -n "$cap" ]; then
    # Escape any single quotes by doubling them; wrap value in single quotes.
    safe=$(echo "$cap" | sed "s/'/''/g")
    echo "\"$fn\": '$safe'" >> "$OUT"
    count=$((count+1))
  fi
done

echo "wrote $count EXIF entries to $OUT"
